const modalContent = document.querySelector(".lightbox__content");
const modalWindow = document.querySelector(".lightbox");
gallery.addEventListener("click", (e) => openModal(e));
const openModal = (e) => {
  const activePhoto = e.target;
  if (activePhoto.className != "gallery__image") return;
  const img = document.querySelector(".lightbox__image");
  img.src = activePhoto.dataset.source;
  img.alt = activePhoto.alt;
  modalWindow.classList.add("is-open");
  closeModal();
};
const closeModal = () => {
  let targetName;
  document.addEventListener("click", (e) => {
    targetName = e.target.className;
    if (targetName == "lightbox__button" || targetName == "lightbox__overlay")
      modalWindow.classList.remove("is-open");
  });
  document.addEventListener("keydown", (e) => {
    targetName = e.key;
    if (targetName == "Escape") modalWindow.classList.remove("is-open");
  });
};

